async function generatePlan() {

    const exam =
        document.getElementById("exam").value;

    const hours =
        parseInt(
            document.getElementById("hours").value
        );

    const subjects = [

        {
            name:
                document.getElementById("subject1").value,

            exam_date:
                document.getElementById("date1").value,

            priority:
                document.getElementById("priority1").value,

            preparation:
                document.getElementById("prep1").value
        },

        {
            name:
                document.getElementById("subject2").value,

            exam_date:
                document.getElementById("date2").value,

            priority:
                document.getElementById("priority2").value,

            preparation:
                document.getElementById("prep2").value
        },

        {
            name:
                document.getElementById("subject3").value,

            exam_date:
                document.getElementById("date3").value,

            priority:
                document.getElementById("priority3").value,

            preparation:
                document.getElementById("prep3").value
        }

    ];

    // Validation

    if (!exam || !hours) {
        alert("Please fill Exam Name and Hours Per Day.");
        return;
    }

    if (
        !subjects[0].name ||
        !subjects[1].name ||
        !subjects[2].name
    ) {
        alert("Please enter all subject names.");
        return;
    }

    console.log({
        exam,
        subjects,
        hours_per_day: hours
    });

    try {

        const response = await fetch(
            "http://127.0.0.1:8000/generate-plan",
            {
                method: "POST",

                headers: {
                    "Content-Type": "application/json"
                },

                body: JSON.stringify({
                    exam: exam,
                    subjects: subjects,
                    hours_per_day: hours
                })
            }
        );

        if (!response.ok) {
            throw new Error(
                `Server Error: ${response.status}`
            );
        }

        const data = await response.json();

        let html = `
            <h2>📚 Personalized Study Plan</h2>

            <div class="progress">
                📈 Progress: 0% Completed
            </div>
        `;

        data.study_plan.forEach((item) => {

            html += `
                <div class="plan-card">

                    <input
                        type="checkbox"
                        class="task-checkbox">

                    <span>${item}</span>

                </div>
            `;

        });

        html += `
            <div class="quote">
                "Success is the sum of small efforts repeated every day."
            </div>

            <div class="footer">
                🚀 Generated by StudySprint AI
            </div>
        `;

        document.getElementById("result").innerHTML =
            html;

        updateProgress();

    } catch (error) {

        console.error(error);

        document.getElementById("result").innerHTML = `
            <div class="plan-card">
                ❌ ${error.message}
            </div>
        `;
    }
}

function updateProgress() {

    const checkboxes =
        document.querySelectorAll(
            ".task-checkbox"
        );

    checkboxes.forEach((checkbox) => {

        checkbox.addEventListener(
            "change",
            () => {

                const total =
                    document.querySelectorAll(
                        ".task-checkbox"
                    ).length;

                const completed =
                    document.querySelectorAll(
                        ".task-checkbox:checked"
                    ).length;

                const percent =
                    Math.round(
                        (completed / total) * 100
                    );

                document.querySelector(
                    ".progress"
                ).innerHTML =
                    `📈 Progress: ${percent}% Completed`;

            }
        );

    });

}
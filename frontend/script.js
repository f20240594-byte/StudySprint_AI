async function generatePlan() {

    const exam = document.getElementById("exam").value;

    const subjects = document
        .getElementById("subjects")
        .value
        .split(",");

    const hours = parseInt(
        document.getElementById("hours").value
    );

    const examDate =
        document.getElementById("examDate").value;

    if (!exam || !hours || !examDate) {
        alert("Please fill all fields.");
        return;
    }

    try {

        const response = await fetch(
            "http://127.0.0.1:8000/generate-plan",
            {
                method: "POST",
                headers: {
                    "Content-Type": "application/json"
                },
                body: JSON.stringify({
                    exam: exam,
                    subjects: subjects,
                    hours_per_day: hours
                })
            }
        );

        const data = await response.json();

        const today = new Date();
        const examDay = new Date(examDate);

        const daysLeft = Math.ceil(
            (examDay - today) /
            (1000 * 60 * 60 * 24)
        );

        let html = `
            <h2>📅 Personalized Study Plan</h2>
            <h3>⏳ ${daysLeft} days left until exam</h3>

            <div class="progress">
                📈 Progress: 0% Completed
            </div>
        `;

        data.study_plan.forEach((item) => {

            html += `
                <div class="plan-card">
                    <input
                        type="checkbox"
                        class="task-checkbox">

                    <span>${item}</span>
                </div>
            `;

        });

        html += `
            <div class="quote">
                "Success is the sum of small efforts repeated every day."
            </div>

            <div class="footer">
                🚀 Generated by StudySprint AI
            </div>
        `;

        document.getElementById("result").innerHTML =
            html;

        updateProgress();

    } catch (error) {

        console.error(error);

        document.getElementById("result").innerHTML = `
            <div class="plan-card">
                ❌ Backend API is not running.
            </div>
        `;
    }
}

function updateProgress() {

    const checkboxes =
        document.querySelectorAll(".task-checkbox");

    checkboxes.forEach((checkbox) => {

        checkbox.addEventListener("change", () => {

            const total =
                document.querySelectorAll(
                    ".task-checkbox"
                ).length;

            const completed =
                document.querySelectorAll(
                    ".task-checkbox:checked"
                ).length;

            const percent =
                Math.round(
                    (completed / total) * 100
                );

            document.querySelector(".progress")
                .innerHTML =
                `📈 Progress: ${percent}% Completed`;

        });

    });

}
